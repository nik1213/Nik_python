# Print numbers from 1 to 10 using a for loop
# Here we will print using FOR loop

#----------------FOR loop----------------------------->
'''
i=1
for i in range(1,11):
    print(i)
    i=i+1
    
'''

#------------------------------While Loop--------------------------------->
'''
i=1
while i<11:
    print(i)
    i=i+1
'''