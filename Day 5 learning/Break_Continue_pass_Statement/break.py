#-Use of break

for i in range(1,21):
    print(i)
    if i==11:
        break 
    
#the execution will be done till 10 and once i=11 the execution will stop
'''
1️⃣ break
🎯 Purpose:

Immediately exit the loop completely.

When Python sees break:

It stops the loop instantly.

Control moves to the first line after the loop
------------------====================================
Key Points:

Terminates the loop entirely.

Works in both for and while.

Often used in search problems.

'''